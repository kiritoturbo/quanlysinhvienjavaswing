    private void loadDuLieu() {
        tbldulieu.setDefaultEditor(Object.class, null); //không cho người dùng chỉnh sửa 
        
        
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=quanlysinhvien";
            String username = "sa";
            String password = "123456789";

            Connection con = DriverManager.getConnection(url, username, password);
            String sql = "select*from lop";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();//trả về kết quả ResultSet
            tblModel.setRowCount(0);
            while (rs.next()) {
                String[] row = new String[]{
                    rs.getString("malop"), rs.getString("makhoa"), rs.getString("manganh"), rs.getString("tenlop"), rs.getString("gvcn"), rs.getString("sdt")
                };
                tblModel.addRow(row);
            }

            tblModel.fireTableDataChanged();//cập nhật dữ liệu trong bảng
            rs.close();
            ps.close();
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
        lblThongbao.setText("Có " + tbldulieu.getRowCount() + " bản ghi.");
    }

