    private Boolean ktTrungThem(String manhap) {
        try {
            int row = 0;
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=quanlysinhvien";
            String username = "sa";
            String password = "123456789";

            Connection con = DriverManager.getConnection(url, username, password);
            String sql = "Select * from lop where malop=N'" + manhap + "'";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();//trả về kết quả ResultSet
            tblModel.setRowCount(0);
            while (rs.next()) {
                row++;
            }
            if (row > 0) {
                return true;
            } else {
                return false;
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
        return null;

    }

